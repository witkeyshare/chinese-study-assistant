import turtle

turtle.fillcolor('red')
turtle.speed(1)
turtle.begin_fill()
turtle.forward(180)
turtle.left(90)
turtle.forward(180)
turtle.goto(0, 0) #
turtle.end_fill()
turtle.fillcolor('yellow')
turtle.begin_fill()
turtle.forward(180)
turtle.right(90)
turtle.forward(180)
turtle.end_fill()
turtle.hideturtle()
turtle.done()